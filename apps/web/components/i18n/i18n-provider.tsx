export type I18nKey = keyof Dictionary;

interface I18nContextValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: I18nKey, params?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("zh");

  useEffect(() => {
    const stored = window.localStorage.getItem(localeStorageKey);
    if (stored === "zh" || stored === "en") {
      setLocaleState(stored);
      return;
    }

    const browserLocale = window.navigator.language.toLowerCase().startsWith("zh") ? "zh" : "en";
    setLocaleState(browserLocale);
  }, []);

  useEffect(() => {
    document.documentElement.lang = locale === "zh" ? "zh-CN" : "en";
    window.localStorage.setItem(localeStorageKey, locale);
  }, [locale]);

  const value = useMemo<I18nContextValue>(() => {
    function t(key: I18nKey, params?: Record<string, string | number>): string {
      const template = dictionaries[locale][key] ?? dictionaries.zh[key] ?? key;
      if (!params) {
        return template;
      }

      let text: string = template;
      for (const [name, replacement] of Object.entries(params)) {
        text = text.replaceAll(`{${name}}`, String(replacement));
      }
      return text;
    }

    return {
      locale,
      setLocale: setLocaleState,
      t,
    };
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const context = useContext(I18nContext);

  if (!context) {
    throw new Error("useI18n must be used within I18nProvider.");
  }

  return context;
}
